public class Test1 {

    public void sortColors(int[] nums) {
        HashMap<Integer, Integer> map = new HashMap<>();
        for (int n : nums) {
            map.put(n, map.getOrDefault(n, 0) + 1);
        }
        List<Integer> colors = new LinkedList<>(map.keySet());
        Collections.sort(colors);
        int i = 0;
        for (Integer n : colors) {
            int k = map.get(n);
            for (int j = 0; j < k; j++) {
                nums[i++] = n;
            }
        }
    }
}
